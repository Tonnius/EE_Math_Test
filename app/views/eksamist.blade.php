<h1>Kitsa ja laia Matemaatika Riigieksamist 2014</h1>
 
<h2>Riigieksami eesmärgid </h2>
 
Riigieksami eesmärgid on: 
<ol>
<li>tagada eksamitulemuste üleriigiline võrreldavus, et õpilasel, lapsevanemal, koolil ja kooli pidajal 
saada võimalikult objektiivne ettekujutus õpitulemuste saavutatusest; </li>
 <li> koolil, kooli pidajal, Haridus- ja Teadusministeeriumil, õpilastel, lastevanematel saada tagasisidet 
õppimise ning õpetamise tulemuslikkusest koolis; </li>
 <li>saada ülevaade riiklikus õppekavas sätestatud õpitulemuste saavutatusest eksamiainetes 
hariduspoliitiliste otsuste tegemiseks; </li>
 <li>toetada õppekava rakendamist, suunata eksamiülesannete valiku kaudu õppeprotsessi; </li>
 <li>siduda järjestikuste haridustasemete õppekavad ning võimaldada kasutada riigieksamitulemusi 
õpingute jätkamisel, anda võimalus kasutada riigieksamitulemusi rahvusvahelisel tasandil. </li>
 </ol>
<h2>Riigieksami vorm ja korraldus </h2>
 
<p>Matemaatika riigieksam on kõigile gümnasistidele kohustuslik kaheosaline kirjalik eksam. 
Matemaatika riigieksami <b>põhieksam toimub 23. mail 2014. a.</b> Eksam algab kell <b>10.00.</b> Eksam on 
kahes variandis – kitsa matemaatika ja laia matemaatika eksam. Eksamivariandi valib eksaminand 
2014. aasta 20. jaanuariks. Kui eksaminand ei saa mõjuvatel põhjustel (nt haigestumine) nimetatud 
kuupäeval eksamile tulla, tuleb esitada 3 tööpäeva jooksul SA Innove avaldus ja eksamind saab tulla 
<b>lisaeksamile</b>, mis toimub <b>2. juunil 2014. a algusega kell 10.00.</b> </p>
 
<p>Eksamiülesanded valmistab ette ja saadab koolidesse SA Innove. Eksami viib läbi kooli 
eksamikomisjon. 
<b>Eksamiülesanded on jaotatud kahte ossa (eraldi vihikud mõlema osa lahenduste jaoks). Esimeses 
osas tuleb eksaminandil lahendada 7 (seitse) ülesannet (4 ülesannet, 5 punkti ülesande kohta ja 3 ülesannet 
10 punkti ülesande kohta). Teises osas tuleb eksaminandil lahendada 5 (viis) ülesannet (10 punkti ülesande kohta). Kokku on 
eksamil vaja lahendada 12 ülesannet. Mõlema osa lahenduste eest on võimalik saada 
maksimaalselt 50 punkti (ja kokku 100 punkti). Esimese osa lahendamiseks on aega 120 minutit 
ja teise osa lahendamiseks 150 minutit. Kahe eksamiosa vahel on 45 minutine vaheaeg.</b> </p>
 
<p>Eksaminandid istuvad eksamiruumis ühe kaupa ja laudadevaheline kaugus peab olema piisav, et 
õpilased saaksid iseseisvalt ja häirimatult töötada. 
<b>SA Innove tagab igale eksaminandile eksamivihikud, kus on ülesanded ja spetsiaalsed kohad 
lahenduste vormistamiseks ning mõlemas osas ka paberi mustandi jaoks.</b> </p>
 
<p>Eksamile ei ole lubatud kaasa võtta ega eksamil kasutada teatmikke, käsiraamatuid jm abimaterjale. 
Lisaks ei tohi eksamiruumis nähtaval kohal olla skeeme, pilte, tabeleid jm matemaatilist 
informatsiooni sisaldavaid materjale. 
<b>Üldjuhul ei ole eksaminandil lubatud eksami ajal eksamiruumist lahkuda. Kui see siiski on 
mingil põhjusel vajalik, siis märgib eksamikomisjon eksamitööle eksamiruumist lahkumise ja 
sinna naasmise aja ning märgib eksami toimumise protokolli eksamiruumist lahkumise põhjuse.</b> </p>
 
<p>Töid kontrollib, parandab ja hindab haridus- ja teadusministri poolt kinnitatud hindamiskomisjon. 
Komisjon ei loe ega hinda hariliku pliiatsiga kirjutatud lahendusi ega mustandipaberile kirjutatut. Riigieksam on sooritatud, kui saavutatud on vähemalt 1% (üks protsent) maksimaalsest tulemusest.
</p>
<h2>Nõutavad teadmised ja oskused </h2>
 
<p>Eksamiülesannete koostamisel lähtutakse riiklikus õppekavas esitatud matemaatika ainekava nõuetest 
(Alus: Vabariigi Valitsuse määrus nr 1; vastu võetud 6. jaanuaril 2011) </p>
 
<h2>Eksamiks vajalikud vahendid </h2>
<ul>
<li>Õpilane kasutab eksamil isiklikke kirjutus- ja joonestusvahendeid ning taskuarvutit. </li>
<li>Eksamitöö kirjutatakse sinise või musta pasta- või tindipliiatsiga. Harilikku pliiatsit ja 
värvipliiatseid võib kasutada vaid jooniste tegemisel. Korrektuurlindi või -vedeliku kasutamine 
ei ole lubatud. </li>
<li>Õpikuid, käsiraamatud, valemeid jm matemaatilise sisuga materjale eksamil kasutada ei tohi. </li>
<li>Mobiiltelefoni jm tehniliste vahendite kasutamine mistahes eesmärgil on eksamil keelatud. </li>
<li>Õpitud valemeid tuleb teada peast. Kui taskuarvutil on klahvid, mis võimaldavad arvutada ilma 
valemeid kasutamata, siis vajalikud valemid ja arvutustehted tuleb eksamitöösse ikkagi 
kirjutada. </li>
</ul>
