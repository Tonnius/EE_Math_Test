<h1>Kuidas õppida</h1>

<h2>Soovitused õppimiseks</h2>

<ul>
	<li>Ära lükka eksamiteks valmistumist ainult eksamiperioodile, viimaseks kuuks või nädalaks. Eksamite maht on suur ja hõlmab kõiki su seniseid kooliaastaid.</li>
	<li>Eksamiainete õpetajatega peaksid nüüd rohkem suhtlema, neile oma küsimustest ja võimalikest teadmistelünkadest märku andma, küsima õppetööks lisamaterjale ja muud sellist.</li>
	<li>Koosta endale õppimist arvestav päevakava. Märgi sinna, millal lõpeb kool, mitu tundi jätad endale suhtlemiseks ja huvitegevuseks, mitu tundi õppimiseks. Arvesta erinevate nädalapäevade rutiiniga (trenniajad, huvikool jms). Püüa siiski jätta õppimiseks iga päev enam-vähem sama kellaaeg, nii harjud paremini uue päevarütmiga. Pea oma päevakavast kinni!</li>
	<li>Mõtle õpiviiside peale. Kas alustad kirjalikest ülesannetest või teooria kordamisest? Kas alustad raskema või kergema ainega? Tee märkmeid, kasuta järjehoidjaid või liimiribaga märkmelipikuid oluliste kohtade tähistamiseks.</li>
	<li>Enne konkreetset õppetükki püstita endale eesmärk, mõtesta lahti, mida tahad leida või teada saada. Nii on vastuseid kergem leida.</li>
	<li>Tõhus õpilugemine on kolmekordne - esiteks ülevaatlik ning tutvuv, teiseks süvenenud ja kolmandaks kordav. Ole mõttega asja juures. Esita endale loetu kohta küsimusi, proovi õpitut seostada eluliste nähtuste või teiste ainetega.</li>
	<li>Mõtle, kas saaksid oma õpitingimusi parandada. Püüa töötada kohas, kus sind kõige vähem segatakse. Kui kodus pole võimalik õppida, kasuta selleks raamatukogu lugemissaali. Hoolitse, et su töövahendid (pliiatsid, paberid, õpikud, konspektid) oleksid alati kindlas kohas ja käepärased, et su töökoht oleks mugav ja valge.</li>
	<li>Luba endale puhkust, kui tunned, et pea on väsinud, ainult sea endale selleks siiski ajalised piirid. Ära anna järele kiusatusele pause pikemaks venitada. Võiksid kohe planeerida endale puhkepausid kindla ajavahemiku tagant ja nende sisustamiseks konkreetse tegevuse: kerged võimlemisharjutused, võileiva tegemine, telefonikõne sõbrale jms.</li>
</ul>

<p>
	Soovitavalt võiks lugeda ka eelmiste aastate riigieksamite kokkuvõtteid (vt <a href="http://www.innove.ee/et/riigieksamid/riigieksamite-statistika">siit</a>), kus on lisaks statistilistele andmetele juttu ka eksaminandide poolt tehtud vigadest ning jagatud soovitusi nende vältimiseks.
</p>

